''' 
    Slicing is used to extract a piece of characters from the string
    Syntax -> variable_name(start:stop:step)
                            start - starting index (inclusive)
                            stop  - ending index   (exclusive)
                            step  - interval b/w characters 
'''

text = 'Shreya is beautiful'
print(text[0:10:2])


name = 'Shreya'
print(name[::-1]) #will reverse the string
