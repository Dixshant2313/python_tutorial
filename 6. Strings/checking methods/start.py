text = "Shreya is my baby"
print(text.startswith("S")) # -> check the starting of string

print(text.endswith("a"))   # -> check the ending of string

print(text.isalpha())       # -> check if the string has only alphabets

print(text.isdigit())       # -> check if the string has only digits

print(text.isalnum())       # -> check if the string contains both digits and alphabets

# Note:- All these above string methods are case sensitive